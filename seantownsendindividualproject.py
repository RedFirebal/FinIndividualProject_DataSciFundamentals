# Reestablish frame

na_year_sales= na_sales.groupby('Year')['NA_Sales'].sum().reset_index()

# Set up our frame - using the pre-aggregated na_year_sales DataFrame
series = na_year_sales['NA_Sales'].values.reshape(-1, 1)

# AI SCALING
scaler = MinMaxScaler(feature_range=(0, 1))
scaled_series = scaler.fit_transform(series)

# Define the means to create a sequence using an array and a given length
def create_sequences(data_array, seq_length):

  X = []
  y = []

  for i in range(len(data_array) - seq_length):
    X.append(data_array[i:i+seq_length])
    y.append(data_array[i+seq_length])

  return np.array(X), np.array(y)

# define what will be used to create the sequence from the SCALED data
window_size = 3

X, y = create_sequences(scaled_series, window_size)

# Begin mapping training parameters
train_size = int(0.7 * len(X))
X_train = X[:train_size]
y_train = y[:train_size]
X_test = X[train_size:]
y_test = y[train_size:]

# Set parameters to proper shape
X_train = X_train.reshape(X_train.shape[0], X_train.shape[1], 1)
X_test = X_test.reshape(X_test.shape[0], X_test.shape[1], 1)

# define the model type
model = Sequential([
  LSTM(50, activation="tanh", input_shape=(window_size, 1)),
  Dense(1)
])

# Compile the model
model.compile(optimizer="adam", loss="mse")

# Train the model using data and defined parameters
history = model.fit(
    X_train,
    y_train,
    epochs=100,
    batch_size=3,
    validation_split=0.1,
    verbose=1
)

# Plot training and validation losses to analyze model effectiveness

plt.figure(figsize=(15, 10))
plt.plot(history.history["loss"], label="Training Loss")
plt.plot(history.history["val_loss"], label="Validation Loss")
plt.title("Training and Validation Loss")
plt.xlabel("Epoch")
plt.ylabel("Loss")
plt.legend()
plt.show()

# Test Model
# AI SCALING
scaled_predictions = model.predict(X_test)

# Plot model vs data

# Test at original scale
# AI SCALING
y_test_original = scaler.inverse_transform(y_test)
predictions_original = scaler.inverse_transform(scaled_predictions)

