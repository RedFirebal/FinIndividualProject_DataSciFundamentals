# Using the setup in the posted Colab 'intro_time_series_lstm_lab.ipynb', set up a time series LSTM to predict how NA sales evolve over time



# ANYTHING RELATING TO SCALING WAS ADJUSTED BY GEMINI AS A FINAL CHECK TO SEE IF IT WAS POSSIBLE TO USE LSTM
